package com.sample;


public class TbFund {

  private long fundId;
  private String number;
  private String fundPurpose;
  private String fundType;
  private String needAmount;
  private String sequenceNumber;
  private String startBit;


  public long getFundId() {
    return fundId;
  }

  public void setFundId(long fundId) {
    this.fundId = fundId;
  }


  public String getNumber() {
    return number;
  }

  public void setNumber(String number) {
    this.number = number;
  }


  public String getFundPurpose() {
    return fundPurpose;
  }

  public void setFundPurpose(String fundPurpose) {
    this.fundPurpose = fundPurpose;
  }


  public String getFundType() {
    return fundType;
  }

  public void setFundType(String fundType) {
    this.fundType = fundType;
  }


  public String getNeedAmount() {
    return needAmount;
  }

  public void setNeedAmount(String needAmount) {
    this.needAmount = needAmount;
  }


  public String getSequenceNumber() {
    return sequenceNumber;
  }

  public void setSequenceNumber(String sequenceNumber) {
    this.sequenceNumber = sequenceNumber;
  }


  public String getStartBit() {
    return startBit;
  }

  public void setStartBit(String startBit) {
    this.startBit = startBit;
  }

}
