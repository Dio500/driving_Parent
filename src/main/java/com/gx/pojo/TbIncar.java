package com.sample;


public class TbIncar {

  private long inCarId;
  private long employeeId;
  private long vehicleId;
  private long subjectId;
  private String inCarPeople;


  public long getInCarId() {
    return inCarId;
  }

  public void setInCarId(long inCarId) {
    this.inCarId = inCarId;
  }


  public long getEmployeeId() {
    return employeeId;
  }

  public void setEmployeeId(long employeeId) {
    this.employeeId = employeeId;
  }


  public long getVehicleId() {
    return vehicleId;
  }

  public void setVehicleId(long vehicleId) {
    this.vehicleId = vehicleId;
  }


  public long getSubjectId() {
    return subjectId;
  }

  public void setSubjectId(long subjectId) {
    this.subjectId = subjectId;
  }


  public String getInCarPeople() {
    return inCarPeople;
  }

  public void setInCarPeople(String inCarPeople) {
    this.inCarPeople = inCarPeople;
  }

}
