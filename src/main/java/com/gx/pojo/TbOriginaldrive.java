package com.sample;


public class TbOriginaldrive {

  private long originalDrivingId;
  private long modelId;
  private String originalDrivingNumber;


  public long getOriginalDrivingId() {
    return originalDrivingId;
  }

  public void setOriginalDrivingId(long originalDrivingId) {
    this.originalDrivingId = originalDrivingId;
  }


  public long getModelId() {
    return modelId;
  }

  public void setModelId(long modelId) {
    this.modelId = modelId;
  }


  public String getOriginalDrivingNumber() {
    return originalDrivingNumber;
  }

  public void setOriginalDrivingNumber(String originalDrivingNumber) {
    this.originalDrivingNumber = originalDrivingNumber;
  }

}
