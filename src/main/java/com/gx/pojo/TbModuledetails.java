package com.sample;


public class TbModuledetails {

  private long moduleDetailsId;
  private long moduleId;
  private String moduleDetailsName;


  public long getModuleDetailsId() {
    return moduleDetailsId;
  }

  public void setModuleDetailsId(long moduleDetailsId) {
    this.moduleDetailsId = moduleDetailsId;
  }


  public long getModuleId() {
    return moduleId;
  }

  public void setModuleId(long moduleId) {
    this.moduleId = moduleId;
  }


  public String getModuleDetailsName() {
    return moduleDetailsName;
  }

  public void setModuleDetailsName(String moduleDetailsName) {
    this.moduleDetailsName = moduleDetailsName;
  }

}
